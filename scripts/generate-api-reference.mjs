#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import ts from "typescript";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const repositoryRoot = join(scriptDirectory, "..");
const outputPath = join(repositoryRoot, "docs", "API.md");
const checkOnly = process.argv.includes("--check");
const entries = [
  ["nfse-js", "src/index.ts"],
  ["nfse-js/core", "src/core/index.ts"],
  ["nfse-js/events", "src/events/index.ts"],
  ["nfse-js/parameters", "src/parameters/index.ts"],
  ["nfse-js/parsing", "src/parsing/index.ts"],
  ["nfse-js/signing", "src/signing/index.ts"],
  ["nfse-js/transport", "src/transport/index.ts"],
  ["nfse-js/validation", "src/validation/index.ts"],
  ["nfse-js/schemas", "src/schemas/index.ts"],
];

const configPath = join(repositoryRoot, "tsconfig.json");
const configFile = ts.readConfigFile(configPath, ts.sys.readFile);
if (configFile.error) {
  throw new Error(
    ts.formatDiagnosticsWithColorAndContext([configFile.error], {
      getCanonicalFileName: (fileName) => fileName,
      getCurrentDirectory: () => repositoryRoot,
      getNewLine: () => "\n",
    }),
  );
}
const parsedConfig = ts.parseJsonConfigFileContent(
  configFile.config,
  ts.sys,
  repositoryRoot,
  { noEmit: true },
  configPath,
);
const program = ts.createProgram(parsedConfig.fileNames, parsedConfig.options);
const diagnostics = ts
  .getPreEmitDiagnostics(program)
  .filter((diagnostic) => diagnostic.category === ts.DiagnosticCategory.Error);
if (diagnostics.length > 0) {
  throw new Error(
    ts.formatDiagnosticsWithColorAndContext(diagnostics, {
      getCanonicalFileName: (fileName) => fileName,
      getCurrentDirectory: () => repositoryRoot,
      getNewLine: () => "\n",
    }),
  );
}

const checker = program.getTypeChecker();
const sections = entries.map(([moduleName, source]) => renderEntry(moduleName, source));
const output = `# API Export Reference

This file is generated by \`npm run docs:api\`. It records every public export,
its declaration kind, callable signature where applicable, and source
declaration. Installed packages include the complete TypeScript declarations.

${sections.join("\n\n")}
`;

if (checkOnly) {
  if (normalizeLineEndings(readFileSync(outputPath, "utf8")) !== output) {
    throw new Error("docs/API.md is stale; run npm run docs:api");
  }
  console.log("API reference is current");
} else {
  writeFileSync(outputPath, output, "utf8");
  console.log(`Generated ${relative(repositoryRoot, outputPath)}`);
}

function renderEntry(moduleName, sourcePath) {
  const absolutePath = join(repositoryRoot, sourcePath);
  const sourceFile = program.getSourceFile(absolutePath);
  const moduleSymbol = sourceFile && checker.getSymbolAtLocation(sourceFile);
  if (!sourceFile || !moduleSymbol) {
    throw new Error(`could not inspect ${sourcePath}`);
  }

  const exports = checker
    .getExportsOfModule(moduleSymbol)
    .map((exportedSymbol) => describeExport(exportedSymbol))
    .sort((left, right) => left.name.localeCompare(right.name));
  const rows = exports
    .map(
      ({ name, kind, signature, source }) =>
        `| \`${escapeCell(name)}\` | ${kind} | ${codeCell(signature)} | ${source} |`,
    )
    .join("\n");

  return `## \`${moduleName}\`

| Export | Kind | Signature or type | Declaration |
| --- | --- | --- | --- |
${rows}`;
}

function describeExport(exportedSymbol) {
  const symbol =
    exportedSymbol.flags & ts.SymbolFlags.Alias
      ? checker.getAliasedSymbol(exportedSymbol)
      : exportedSymbol;
  const declaration = symbol.valueDeclaration ?? symbol.declarations?.[0];
  if (!declaration) {
    return {
      name: exportedSymbol.name,
      kind: "symbol",
      signature: "",
      source: "",
    };
  }

  const type = checker.getTypeOfSymbolAtLocation(symbol, declaration);
  const signatures = type.getCallSignatures();
  const sourceFile = declaration.getSourceFile();
  const line = sourceFile.getLineAndCharacterOfPosition(declaration.getStart(sourceFile)).line + 1;
  const sourcePath = relative(repositoryRoot, sourceFile.fileName).replaceAll("\\", "/");

  return {
    name: exportedSymbol.name,
    kind: symbolKind(symbol),
    signature:
      signatures.length > 0
        ? signatures.map((signature) => checker.signatureToString(signature)).join(" / ")
        : typeSummary(symbol, type, declaration),
    source: `[\`${sourcePath}:${line}\`](../${sourcePath}#L${line})`,
  };
}

function symbolKind(symbol) {
  if (symbol.flags & ts.SymbolFlags.Class) return "class";
  if (symbol.flags & ts.SymbolFlags.Function) return "function";
  if (symbol.flags & ts.SymbolFlags.Interface) return "interface";
  if (symbol.flags & ts.SymbolFlags.TypeAlias) return "type";
  if (symbol.flags & ts.SymbolFlags.Enum) return "enum";
  if (symbol.flags & ts.SymbolFlags.Variable) return "constant";
  return "symbol";
}

function typeSummary(symbol, type, declaration) {
  if (symbol.flags & ts.SymbolFlags.Interface) {
    return symbol.name;
  }
  if (symbol.flags & ts.SymbolFlags.Class) {
    const constructors = type.getConstructSignatures();
    return constructors.length > 0
      ? constructors.map((signature) => `new ${checker.signatureToString(signature)}`).join(" / ")
      : symbol.name;
  }
  return checker.typeToString(
    symbol.flags & ts.SymbolFlags.TypeAlias ? checker.getDeclaredTypeOfSymbol(symbol) : type,
    declaration,
  );
}

function codeCell(value) {
  return value ? `\`${escapeCell(normalizeWhitespace(value))}\`` : "";
}

function normalizeWhitespace(value) {
  return value.replace(/\s+/g, " ").trim();
}

function escapeCell(value) {
  return value.replaceAll("|", "\\|").replaceAll("`", "\\`");
}

function normalizeLineEndings(value) {
  return value.replace(/\r\n?/g, "\n");
}
