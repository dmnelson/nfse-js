#!/usr/bin/env node

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const repositoryRoot = join(scriptDirectory, "..");
const schemaDirectory = join(repositoryRoot, "schemas", "1.01");
const outputFile = join(repositoryRoot, "src", "schemas", "generated.ts");

const schemaFiles = readdirSync(schemaDirectory)
  .filter((fileName) => fileName.endsWith(".xsd"))
  .sort();

const entries = schemaFiles
  .map((fileName) => {
    let contents = readFileSync(join(schemaDirectory, fileName), "utf8");

    if (fileName === "tiposSimples_v1.01.xsd") {
      contents = contents.replace(
        '<xs:pattern value="^0{0,4}\\d{1,5}$"/>',
        '<xs:pattern value="0{0,4}\\d{1,5}"/>',
      );
    }

    return `  {
    fileName: ${JSON.stringify(fileName)},
    contents: ${JSON.stringify(contents)},
  },`;
  })
  .join("\n");

const output = `// Generated by scripts/generate-schema-module.mjs. Do not edit.

import type { SchemaFile } from "./types.js";

export const NATIONAL_NFSE_V1_01_SCHEMAS: readonly SchemaFile[] = [
${entries}
];
`;

writeFileSync(outputFile, output, "utf8");
console.log(`Generated ${schemaFiles.length} schemas in ${outputFile}`);
