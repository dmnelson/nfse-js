#!/usr/bin/env node

import { createHash } from "node:crypto";
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const repositoryRoot = join(scriptDirectory, "..");
const schemaDirectory = join(repositoryRoot, "schemas", "1.01");
const manifestFile = join(repositoryRoot, "schemas", "manifest.json");
const outputFile = join(repositoryRoot, "src", "schemas", "generated.ts");
const checkOnly = process.argv.includes("--check");
const manifest = JSON.parse(readFileSync(manifestFile, "utf8"));

const schemaFiles = readdirSync(schemaDirectory)
  .filter((fileName) => fileName.endsWith(".xsd"))
  .sort();
const manifestFiles = Object.keys(manifest.files).sort();
const runtimeFiles = Object.keys(manifest.runtimeFiles).sort();

assertSameFiles("official schema directory", schemaFiles, manifestFiles);
assertSameFiles("runtime schema manifest", schemaFiles, runtimeFiles);

const patchesByFile = new Map();
for (const patch of manifest.compatibilityPatches) {
  if (!manifest.files[patch.file]) {
    throw new Error(`compatibility patch targets unknown schema ${patch.file}`);
  }
  if (patchesByFile.has(patch.file)) {
    throw new Error(`multiple compatibility patch entries target ${patch.file}`);
  }
  if (!Array.isArray(patch.replacements) || patch.replacements.length === 0) {
    throw new Error(`compatibility patch for ${patch.file} has no replacements`);
  }
  patchesByFile.set(patch.file, patch.replacements);
}

const entries = schemaFiles
  .map((fileName) => {
    const officialBytes = readFileSync(join(schemaDirectory, fileName));
    const officialHash = sha256(officialBytes);
    if (officialHash !== manifest.files[fileName]) {
      throw new Error(
        `official schema hash mismatch for ${fileName}: expected ${manifest.files[fileName]}, received ${officialHash}`,
      );
    }

    let contents = officialBytes.toString("utf8");
    for (const replacement of patchesByFile.get(fileName) ?? []) {
      const count = countOccurrences(contents, replacement.search);
      if (count !== replacement.expectedCount) {
        throw new Error(
          `compatibility patch for ${fileName} expected ${replacement.expectedCount} matches but found ${count}`,
        );
      }
      contents = contents.replaceAll(replacement.search, replacement.replace);
    }

    const runtimeHash = sha256(Buffer.from(contents, "utf8"));
    if (runtimeHash !== manifest.runtimeFiles[fileName]) {
      throw new Error(
        `runtime schema hash mismatch for ${fileName}: expected ${manifest.runtimeFiles[fileName]}, received ${runtimeHash}`,
      );
    }

    return `  Object.freeze({
    fileName: ${JSON.stringify(fileName)},
    contents: ${JSON.stringify(contents)},
  }),`;
  })
  .join("\n");

const output = `// Generated by scripts/generate-schema-module.mjs. Do not edit.

import type { SchemaFile } from "./types.js";

export const NATIONAL_NFSE_V1_01_SCHEMAS: readonly SchemaFile[] = Object.freeze([
${entries}
]);
`;

if (checkOnly) {
  if (readFileSync(outputFile, "utf8") !== output) {
    throw new Error("src/schemas/generated.ts is stale; run npm run generate:schemas");
  }
  console.log(`Verified ${schemaFiles.length} generated schemas`);
} else {
  writeFileSync(outputFile, output, "utf8");
  console.log(`Generated ${schemaFiles.length} schemas in ${outputFile}`);
}

function assertSameFiles(label, actual, expected) {
  if (actual.length !== expected.length || actual.some((file, index) => file !== expected[index])) {
    throw new Error(
      `${label} does not match manifest: expected ${expected.join(", ")}, received ${actual.join(", ")}`,
    );
  }
}

function countOccurrences(value, search) {
  if (!search) {
    throw new Error("compatibility patch search value must not be empty");
  }
  return value.split(search).length - 1;
}

function sha256(contents) {
  return createHash("sha256").update(contents).digest("hex");
}
